package class3;

public class OperatorDemo_3 {
    public static void main(String[] args) {

        int number=10;
        System.out.println(number/3);   //when we are using int data type we get this  //3
        System.out.println(number%3);  //1

        System.out.println(16/5);
        System.out.println(16%5);
        System.out.println(156%2); // even number
        System.out.println(155%2);  // odd number // we always take the modulus by 2 if the answer is 0 its an even number otherwise its an odd number.

        System.out.println(1555%5);


    }
}
